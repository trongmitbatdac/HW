		sprintf(Buffer_LCD,"Density:%3.2fug/m3 ",Avg_den);
		lcd_send_string(Buffer_LCD);
		Clear_Buffer_LCDer(Buffer_LCD);
		
		lcd_put_cur(4,0);
		sprintf(Buffer_LCD,"Time:%2d:%2d:%2d",time.hour,time.minutes,time.seconds);
		lcd_send_string(Buffer_LCD);
		Clear_Buffer_LCDer(Buffer_LCD);
